package main

import "fmt"

func main() {
	var number uint = 260 //var declare
	number = number + 5   //add
	fmt.Println("hello ", number)
	number = number - 3 //sub
	fmt.Println("hello ", number)
	number = number / 2 //div
	fmt.Println("hello ", number)
	number = number % 2 //reminder
	fmt.Println("hello ", number)
	number = number * 20 //multiply
	fmt.Println("hello ", number)
}

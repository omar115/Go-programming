/* various values representation
using golang, string, integer, etc.
*/

package main

import "fmt"

func main() {
	fmt.Println("go" + "lang") //string concatenation

	fmt.Println("1+1 = ", 1+1) //addition

	fmt.Println("7.0/3.0 =", 7.0/3.0) //division

	fmt.Println(true && false) //and operation

	fmt.Println(true || false) //or operattion

	fmt.Println(!true)
}
